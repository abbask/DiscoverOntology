package model;

import java.util.List;

public class MyTestSuite{
	
	private String name;
	private List<MyTest> tests;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<MyTest> getTests() {
		return tests;
	}
	public void setTests(List<MyTest> tests) {
		this.tests = tests;
	}
	
	

}
